class Calculator:
    def __init__(self, result=0):
        self.result = result

    def add(self, x, y):
        self.result = x + y

    def subtract(self, x, y):
        self.result = x - y

    def multiply(self, x, y):
        self.result = x * y

    def divide(self, x, y):
        self.result = x / y

